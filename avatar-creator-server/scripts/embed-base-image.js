#!/usr/bin/env node
/**
 * Embeds web/base_character.png as base64 into api/lib/defaultBaseImageData.js
 * so the /api/v1/image handler can use it without fetching (avoids 401 from Deployment Protection).
 * Run before deploy (Vercel buildCommand runs this).
 */
import fs from 'fs';
import path from 'path';

const root = process.cwd();
const src = path.join(root, 'web', 'base_character.png');
const out = path.join(root, 'api', 'lib', 'defaultBaseImageData.js');

if (!fs.existsSync(src)) {
  console.warn('embed-base-image: web/base_character.png not found, skipping');
  process.exit(0);
}

const buf = fs.readFileSync(src);
const base64 = buf.toString('base64');
const content = `// Generated by scripts/embed-base-image.js - do not edit
export const defaultBaseImageBase64 = ${JSON.stringify(base64)};
`;

fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out, content, 'utf8');
console.log('embed-base-image: wrote api/lib/defaultBaseImageData.js');
